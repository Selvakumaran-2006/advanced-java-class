import java.util.Scanner;

public class Nvalue {
    public static void main(String[] args) {
        Scanner scan=new Scanner(System.in);
        int n=scan.nextInt();
        int i=n;
        while (i>1) {
            System.out.println(i+"");
            i++;
               
        }
        scan.close();

        
    }

    

}
